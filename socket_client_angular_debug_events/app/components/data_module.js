'use strict';
/* globals io */

angular.module('socketIoAngularClientApp.data_module', [])

    .factory('IcelandSocket', ['$rootScope', function ($rootScope) {
        var socket = io.connect(':3000/icelandUsers');

        return {
            on: function (eventName, callback) {
                socket.on(eventName, function () {
                    var args = arguments;
                    $rootScope.$apply(function () {
                        callback.apply(socket, args);
                    });
                });
            },
            emit: function (eventName, data, callback) {
                socket.emit(eventName, data, function () {
                    var args = arguments;
                    $rootScope.$apply(function () {
                        if (callback) {
                            callback.apply(socket, args);
                        }
                    });
                });
            }
        };
    }]);